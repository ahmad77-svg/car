/* =========================
   Header and back to top
========================= */

function handleScroll() {
  const scrollPosition = window.scrollY;

  header.classList.toggle("scrolled", scrollPosition > 30);
  backToTop.classList.toggle("visible", scrollPosition > 500);
}

window.addEventListener("scroll", handleScroll);
handleScroll();

backToTop.addEventListener("click", () => {
  window.scrollTo({
    top: 0,
    behavior: "smooth"
  });
});

/* =========================
   Active navigation
========================= */

const sections = document.querySelectorAll("main section[id]");

function updateActiveNavigation() {
  let currentSection = "home";

  sections.forEach((section) => {
    const sectionTop = section.offsetTop - 160;
    const sectionHeight = section.offsetHeight;

    if (
      window.scrollY >= sectionTop &&
      window.scrollY < sectionTop + sectionHeight
    ) {
      currentSection = section.id;
    }
  });

  navLinks.forEach((link) => {
    link.classList.toggle(
      "active",
      link.getAttribute("href") === `#${currentSection}`
    );
  });
}

window.addEventListener("scroll", updateActiveNavigation);

/* =========================
   Reveal animation
========================= */

const revealObserver = new IntersectionObserver(
  (entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) {
        return;
      }

      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    });
  },
  {
    threshold: 0.12
  }
);

revealElements.forEach((element) => {
  revealObserver.observe(element);
});

/* =========================
   Animated counters
========================= */

let countersStarted = false;

function startCounters() {
  if (countersStarted) {
    return;
  }

  countersStarted = true;

  counters.forEach((counter) => {
    const target = Number(counter.dataset.target);
    const duration = 1700;
    const startTime = performance.now();

    function updateCounter(currentTime) {
      const elapsedTime = currentTime - startTime;
      const progress = Math.min(elapsedTime / duration, 1);

      const easedProgress = 1 - Math.pow(1 - progress, 3);
      const currentValue = Math.floor(target * easedProgress);

      counter.textContent = currentValue.toLocaleString("en-US");

      if (progress < 1) {
        requestAnimationFrame(updateCounter);
      } else {
        counter.textContent = target.toLocaleString("en-US");
      }
    }

    requestAnimationFrame(updateCounter);
  });
}

const statisticsSection = document.querySelector(".statistics");

const statisticsObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        startCounters();
      }
    });
  },
  {
    threshold: 0.3
  }
);

statisticsObserver.observe(statisticsSection);

